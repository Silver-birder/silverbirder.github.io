export class TextContent implements Question<string> {
  private role: role;
  private name: string;

  private constructor(role: role, name: string) {
    this.role = role;
    this.name = name;
  }

  static ofRoleWithName(role: role, name: string): TextContent {
    return new TextContent(role, name);
  }

  async answeredBy(actor: Actor): Promise<string> {
    const page = actor.abilityTo(PlaywrightAbility).page;
    const element = await page.getByRole(this.role, { name: this.name });
    const text = await element.textContent();
    return text ?? '';
  }
}
